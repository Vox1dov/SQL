Ma'lumotlar-Real dunyodagi faktlar, raqamlar yoki belgilar to‘plami, xom axborot shaklida bo‘ladi.
Ma'lumotlar bazasi-Ma'lumotlarni tartib bilan saqlash, boshqarish va ularga tezkor kirishni ta'minlovchi tizim.
Relyatsion ma'lumotlar bazasi-Ma'lumotlarni jadval shaklida saqlaydigan ma'lumotlar bazasi turi.
Jadvallar o‘zaro bog‘langan bo‘lib, har bir jadval ustunlar (atributlar) va satrlardan (yuzalar) tashkil topgan.
Relyatsion ma'lumotlar bazasidagi ma'lumotlarni saqlash shakli, ustunlar va satrlardan iborat.
Har bir ustun ma'lum bir ma'lumot turini ifodalaydi, har bir satr esa bitta yozuv hisoblanadi.

2. SQL Serverning beshta asosiy xususiyati:
Yuqori ishlash samaradorligi — katta hajmdagi ma'lumotlarni tez va samarali boshqaradi.
Yuqori xavfsizlik darajasi — ma'lumotlarni himoya qilish uchun kuchli autentifikatsiya va ruxsat tizimlari mavjud.
Zaxira nusxa olish va tiklash imkoniyatlari — ma'lumotlarni yo‘qotishdan saqlash uchun qulay vositalar bor.
Transaktsiyalarni qo‘llab-quvvatlash — ma'lumotlarning yaxlitligini ta'minlaydigan ACID xususiyatlari.
Kengaytirilgan tahlil va hisobot vositalari — ma'lumotlarni tahlil qilish uchun integratsiyalashgan vositalar (SQL Server Reporting Services va boshqalar).

3. SQL Serverga ulanishda autentifikatsiya usullari:
Windows Authentication (Windows autentifikatsiyasi)
Foydalanuvchi Windows hisobiga asoslanib tizimga ulanadi. Bu usul ko‘proq xavfsiz va qulay hisoblanadi
SQL Server Authentication (SQL Server autentifikatsiyasi)
Maxsus foydalanuvchi nomi va parol bilan SQL Serverga ulanadi. Bu usul Windows hisobidan mustaqil ishlaydi.

